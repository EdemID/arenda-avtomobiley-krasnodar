const makeGallery = (
  folder: string,
  prefix: string,
  dimensions: Array<[number, number]>,
  alt: string,
): CarImage[] =>
  dimensions.map(([width, height], index) => ({
    src: `/images/cars/${folder}/${prefix}-${String(index + 1).padStart(2, '0')}.jpg`,
    alt: `${alt}, фото ${index + 1}`,
    width,
    height,
  }))

